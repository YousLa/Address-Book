<p>Exercice récapitulatif complet : Le carnet d'adresses</p>



<p>Notions vues et mise en pratique : création d'une base de données, PHP de base, PDO et MVC.</p>



<p> Développez une application de gestion de contacts.
    Les utilisateurs devraient pouvoir ajouter, modifier et supprimer des contacts, ainsi que les afficher sous forme de liste.
    Chaque contact peut avoir des informations telles que :
<p>→ nom</p>
<p>→ prénom</p>
<p>→ pseudo (optionnel)</p>
<p> → numéro de téléphone (optionnel)</p>
<p> → adresse email (optionnel)</p>
<p>→ adresse postale (optionnel)</p>



<p>→ (bonus) catégorie (optionnel) (choix d'une catégorie (CRUD))</p>



<p>Vous devez créer un compte utilisateur pour créer votre liste de contacts.</p>



<p> Bonus : Permettre de gérer la photo du contact.</p>



<p> Exemple d'existant : https://contacts.google.com/</p>